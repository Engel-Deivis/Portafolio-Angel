// --------------- Abrir Formulario de contacto ---------------
function abrirFormularioContacto() {
    const abrirFormulario = document.getElementById('formulario-contacto')
    abrirFormulario.style.display = 'block'
}


// --------------- Cerrar Formulario de contacto ---------------
function cerrarFormularioContacto() {
    const cerrarFormulario = document.getElementById('formulario-contacto')
    cerrarFormulario.style.display = 'none'
}


